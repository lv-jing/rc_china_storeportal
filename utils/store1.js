/**
 * 获取所有省份
 */
function getProvinces() {
	var provinces = [];
	for (var i = 0; i < StoreJson.length; i++) {
		provinces.push(StoreJson[i]);
	}
	return provinces;
}


/**
 * 获取省对应的所有城市
 */
function getCitys(provinceIndex) {
	var citys = [];
	for (var i = 0; i < StoreJson[provinceIndex].city.length; i++) {
		citys.push(StoreJson[provinceIndex].city[i]);
	}
	return citys;
}

/**
 * 获取省市对应的所有地区
 */
function getAreas(provinceIndex, cityIndex) {
	var areas = [];
	areas = StoreJson[provinceIndex].city[cityIndex].area;
	return areas || [];
}

